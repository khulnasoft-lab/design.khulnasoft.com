      <h2>Principle guidelines</h2>
      <p>The principles defined should align to the following guidelines to assure they are to be most effective.</p>
      <h3>Origin and purpose</h3>
      <p>Purpose and values inform principles, while principles inform the product. Make sure that no conflicting principles are introduced.</p>
      <p>Principles should either inform the process or the desired output. Those that define processes are described in our <a href="https://about.gitlab.com/handbook/product/#product-principles">handbook</a>, while the outcome principles are detailed here.</p>
      <h3>Target audience</h3>
      <p>Different teams require different principles to guide them, while some principles might be useful to more than one. It is important that the target audience is clear to avoid confusion while considering them when making decisions.</p>
      <h3>Actionable and genuine</h3>
      <p>Principles should be actionable and thus strike a balance between being too generic and too specific. Additionally, for them to be genuine and have context, they are required to have real demonstration examples.</p>
      <h3>Give direction by being opinionated</h3>
      <p>When principles have a point of view, they give direction and keep teams on the path to achieving their vision. It defines a decision where other companies could have diverged. By contrast, it is less useful to state things in principles that are logical and wished upon by default. An example of this would be “Useful,” of which the opposite would be “not useful.” Slim are the chances a company wants to create such a product and obvious the fact that a company wants to create a useful product.</p>
      <h3>Explicit hierarchy</h3>
      <p>Each principle should be assigned a hierarchy position to resolve confusion about what to do in a specific
        circumstance while remaining consistent.</p>
      <h3>Memorable</h3>
      <p>Similar to the <a href="https://about.gitlab.com/handbook/values/#about-our-values">GitLab Company Values</a>, principles are most effective when they are easy to remember. Because of this, the number of principles should be kept low, and there is the possibility to add sub-principles. Three to five principles are ideal. Anything that makes them even more memorable is helpful.</p>
