      <h1>Labels</h1>
      <p>Labels categorise UI objects by visually labeling them. They provide the user a quick way to recognise to which category or categoies the current object belongs. Clicking on labels navigates the user towards a filtered list view based on that label.</p>
      <h2>Usage</h2>
      <p>Labels are used to categorise first class objects like issues, merge requests and epics. They can be used as a way to filter against in list views. Upon hover they present a description of the category they represent. They are easy to spot by being fully colored which is either contrasted by white or black text. </p>
      <h3>User role labels</h3>
      <p>User role labels are different from normal labels in that they can only indicate one category, the permission level of the user. Visually they are less obvious by being grayscale and only having a border.</p>
